<p>资源模块化</p>
<p><em>前端开发过程中，我们要面对的文件类型碎片化，相比后台开发主要面对仅仅只是一类类型文件，如 go 对应的就是 .go文件，而前端开发需要面对多种文件类型，如 JS 文件、CSS 文件、HTML文件、图片、字体等</em></p>
<p><em>怎么在前端项目中更高效地管理和维护项目中的每一个文件资源及其关系？</em></p>
<p><em>如，Webpack 是一个模块化打包工具，但它通过“万物皆模块”这种设计思想，巧妙地实现了整个前端项目的模块化，统一标准模块化处理。在 Webpack 的理念中，前端项目中的任何资源都可以作为一个模块，任何模块都可以经过 Loader 机制的处理，最终再被打包到一起</em></p>
